/// Look up glyph data for a codepoint. Returns null for unsupported characters.
pub fn glyph(cp: u8) ?*const [8]u8 {
    if (cp == 0x10) return &cursor;
    if (cp < 32 or cp > 126) return null;
    return &glyphs[cp - 32];
}

const std = @import("std");

test "glyph returns data for printable ASCII" {
    const a = glyph('A');
    try std.testing.expect(a != null);
    // A: row 0 is ..##.... = 0x30
    try std.testing.expectEqual(@as(u8, 0x30), a.?[0]);
}

test "glyph returns null for out of range" {
    try std.testing.expect(glyph(0) == null);
    try std.testing.expect(glyph(31) == null);
    try std.testing.expect(glyph(127) == null);
}

test "cursor glyph accessible via 0x10" {
    const c = glyph(0x10);
    try std.testing.expect(c != null);
    // Cursor: row 0 is .#...... = 0x40
    try std.testing.expectEqual(@as(u8, 0x40), c.?[0]);
}

test "space glyph is all zeros" {
    const s = glyph(' ');
    try std.testing.expect(s != null);
    for (s.?) |byte| {
        try std.testing.expectEqual(@as(u8, 0), byte);
    }
}

